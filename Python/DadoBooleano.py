# Tipo de dado Boolean ou bool
# São dados que retornar true or false, e existem apenas essas duas respostas.
# Ao questionar ao programa ele ira devolver apenas sim(True) ou não(False).
# Existem vários operadore para "questionar".
# Dentre eles o == , que vai questionar se o valor é igual ao outro.

print(10 == 9) # False -> falso = não 
print(10 == 10) # True -> verdadeiro = sim

# type para checar qual o tipo de classe pertence. 

print(type(10==10)) # retorna a <classe = bool>
print(type(False), type(True)) # bool