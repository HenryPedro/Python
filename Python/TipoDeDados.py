# Tipos int e float 
# Int são números inteiros
# O Int representa qualquer número, seja positivo ou negativo. 
# Int sem sinal é considerado positivo.
# NÃO TEM CASA DECIMAL = int 

print(11) #int
print(-5) #int
print(0) #int


# float são numeros com ponto flutuante
# O float representa qualquer núemro também, seja positivo ou negativo com ponto flutuante.
# float sem sinal também é considerado Number Positivo.
# CASA DECIMA = float 

print(2.5) # float 
print(1.1, 1.2, 1.3) # float   
print(10.11, -10.11) # float

# A função type mostra o tipo que o Python interferiu ao valor.
print(type(15))
print(type(1.5))
print( type('PedroH'),  type (0.2) , type(-10) ) 
